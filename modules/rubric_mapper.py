def get_rubric_id(rubric_name: str) -> int:
    """
    Возвращает ID рубрики по её названию.
    
    Args:
        rubric_name: Название рубрики (регистронезависимо)
        
    Returns:
        ID рубрики или ID новостей (382235) по умолчанию
    """
    if not rubric_name:
        return 382235
        
    rubric_name = rubric_name.lower().strip()
    return RUBRIC_MAP.get(rubric_name, 382235)

def get_rubric_name_by_id(rubric_id: int) -> str:
    """
    Возвращает название рубрики по её ID.
    
    Args:
        rubric_id: ID рубрики
        
    Returns:
        Название рубрики или "новости" если не найдено
    """
    for name, rid in RUBRIC_MAP.items():
        if rid == rubric_id:
            return name
    return "новости"

